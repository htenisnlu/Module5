# This program performs operations on a list of numbers.

numbers = [12, 10, 32, 3, 66, 17, 42, 99, 20]

# Part a: Print each number on a new line
for number in numbers:
    print(number)

# Part b: Print each number and its square on a new line
for number in numbers:
    print(f"{number} {number**2}")